<?php

namespace CurrencyConverterServer\core\base;

class Result
{
    public bool $status = false;

    public string $message = "";

    public array $data = [];

    public function isStatus(): bool
    {
        return $this->status;
    }

    public function setStatus(bool $status): void
    {
        $this->status = $status;
    }

